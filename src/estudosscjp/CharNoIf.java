package estudosscjp;

public class CharNoIf {
public static void main(String[] args) {
	char caracter1 = 'A';
	char caracter2 = 'B';
	if (caracter1 < caracter2) {
	System.out.println("true");
	} else {
	System.out.println("false");
	}
	
	System.out.println(10.0 == 10L);
	System.out.println((1>4) ^ (4>2));
}
}
